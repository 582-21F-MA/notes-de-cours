// Commençons par un exemple traditionnel pour ceux et celles qui
// apprennent un nouveau langage de programmation : hello, world.

// Quoique ceci n'est pas obligatoire, il est conventionnel d'appeler la
// fonction principal d'un programme « main ». Puisque celle-ci ne
// retourne rien, on indique que le type de sa valeur de retour
// est « void ».
function main(): void {
    // La fonction native « console.log » écrit la valeur donnée comme
    // argument, soit dans la console du navigateur web, soit dans le
    // flux de sortie du terminal.
    console.log("Hello, World");
}

main();
